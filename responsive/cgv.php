				Lors d'un désistement 15 jours ouvrés avant la session, la totalité des arrhes sont rendus au stagiaire.
				Lors d'un désistement intervenant entre 15 jours et le premier jour de la session, le montant des arrhes est totalement dû et l'organisateur se réserve le droit de facturer le montant correspondant aux frais d'hébergement et de restauration.
				<br/>
				<br/>
				<h4 class="magenta2"><b>En cours de stage :</b></h4>
				<br/>
				<br/>
