				<br/>
				<br/>
				<h4 class="yellow2"><b>Annulation d'une session :</b></h4>
				<br/>
				<br/>
				L'organisateur se réserve le droit d'annuler une session ou en annuler les modalités d'organisation (dates, lieu, type de session, thèmes...). Dans ce cas, les participants sont informés par courrier ou par téléphone dans les plus brefs délais et l'organisateur propose aux participants une solution personnalisée.     
				Dans le cas où aucune solution n'est trouvée entre les deux parties, les arrhes versées par le stagiaire sont remboursées intégralement par l'organisateur.
				<br/>
				<br/>
				<h4 class="blue2"><b>Avances de frais frais médicaux :</b></h4>
				<br/>
